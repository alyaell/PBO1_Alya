<?php
// Latihan 2.6 - Operator Array

$x = array("a" => "red", "b" => "green");
$y = array("c" => "blue", "d" => "yellow");

// Perbandingan array
var_dump($x != $y);
echo "<br>";

var_dump($x === $y);
echo "<br>";

var_dump($x <> $y);
echo "<br>";

var_dump($x !== $y);
echo "<br>";
?>
